function displayData(data) {
	womanProduct.innerHTML = null;
	// console.log("click")

	data.forEach(function (ele) {
		var womanCard = document.createElement("div");

		var womanImg = document.createElement("img");
		womanImg.src = ele.productImage;
		womanImg.setAttribute("id", "insideProductPage")
		womanImg.addEventListener("click", function () {
			funInsideProductPage(ele);
		})

		var hr = document.createElement("hr")
		hr.setAttribute("class", "womanhr")

		var womanName = document.createElement("p");
		womanName.textContent = ele.productName;
		womanName.setAttribute("class", "womanCardName")

		var womanCatagory = document.createElement("p");
		womanCatagory.textContent = ele.productCatogary;
		womanCatagory.setAttribute("class", "womanCardCatagory")

		var womanActualPriceDiv = document.createElement("div");
		womanActualPriceDiv.setAttribute("class", "womanCardActualDiv")

		var womanActualPriceUnit = document.createElement("p");
		womanActualPriceUnit.textContent = "US $";

		var womanActualPrice = document.createElement("p");
		womanActualPrice.textContent = ele.productActualPrice;

		var womanDicountPercentage = document.createElement("p");
		womanDicountPercentage.textContent = ele.productDiscountOff;

		womanActualPriceDiv.append(
			womanActualPriceUnit,
			womanActualPrice,
			womanDicountPercentage
		);

		var womanDiscountPriceDiv = document.createElement("div");
		womanDiscountPriceDiv.setAttribute("class", "womanCardDiscountDiv")

		var womanDiscountPriceUnit = document.createElement("p");
		womanDiscountPriceUnit.textContent = "US $";

		var womanDicountPrice = document.createElement("p");
		womanDicountPrice.textContent = ele.productDiscountPrice;

		womanDiscountPriceDiv.append(
			womanDiscountPriceUnit,
			womanDicountPrice,
		);
		womanCard.append(
			womanImg,
			hr,
			womanName,
			womanCatagory,
			womanActualPriceDiv,
			womanDiscountPriceDiv,
		);
		womanProduct.append(womanCard);
	});
}

document.querySelector("#sortByPrice").addEventListener("change", funSortByPrice);

function funSortByPrice() {
	console.log("click")

	var selected = document.querySelector("#sortByPrice").value;
	console.log(selected);

	if (selected == "LTH") {
		womenProductArr.sort(function (a, b) {
			return a.productDiscountPrice - b.productDiscountPrice;
		})
		displayData(womenProductArr);
	}
	if (selected == "HTL") {
		womenProductArr.sort(function (a, b) {
			return b.productDiscountPrice - a.productDiscountPrice;
		})
		displayData(womenProductArr);
	}

}

document.querySelector("#sortByType").addEventListener("change", funSortByType);

function funSortByType() {
	console.log("click")
	var selected = document.querySelector("#sortByType").value;
	console.log(selected)
	var filteredList = womenProductArr.filter(function (ele) {
		return ele.productType == selected;
	})
	displayData(filteredList);
}

